	private static final org.slf4j.Logger logger = LoggerFactory.getLogger(HV.class);
	
	public static double calculate(final double[] S) {
		final int N = S.length;
		
		logger.debug("N = {}", N);
		
		double[] lnS = new double[N];
		for(int i = 0; i < N; i++) lnS[i] = Math.log(S[i]);
		
		double[] R = new double[N];
		R[0] = 0;
		for(int t = 1; t < N; t++) {
			R[t] = lnS[t] - lnS[t - 1];
		}
		double avrR = 0;
		for(int t = 1; t < N; t++) {
			avrR += R[t];
		}
		avrR /= N;
		logger.debug("avrR = {}", avrR);

		double sumR = 0;
		for(int t = 1; t < N; t++) {
			final double r = R[t] - avrR;
			sumR += r * r;
		}
		
		final double ret = Math.sqrt(250 / (N - 1) * sumR);
		logger.debug("ret = {}", ret);
		return ret;
	}
