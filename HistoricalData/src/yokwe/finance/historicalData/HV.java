	public static final double[] toArray(List<Double> data) {
		double ret[] = new double[data.size()];
		int i = 0;
		for(double t: data) ret[i++] = t;
		
		return ret;
	}
	// sum - summation
	public static final double sum(final double[] data) {
		double ret = 0;
		
		for(double t: data) ret += t;
		return ret;
	}
	// avg - average
	public static final double avg(final double[] data) {
		return sum(data) / (double)data.length;
	}
	// var - variance (unbiased sample variance)
	public static double varSample(final double[] data) {
		return varBase(data, 1);
	}
	public static double varPopulation(final double[] data) {
		return varBase(data, 0);
	}
	private static double varBase(final double[] data, final int offset) {
		// minus one for unbiased sample variance 
		final double N = data.length - 1;
		final double m = avg(data);
				
		double sum = 0;
		for(double t: data) {
			double u = t - m;
			sum += (u * u);
		}

		final double ret = sum / N;

		logger.info("N = {}", N);
		logger.info("m = {}", m);
		logger.info("sum = {}", sum);
		logger.info("var = {}", ret);
		
		return ret;
	}
	// stddev - standard deviation
	private static double stddevBase(final double[] data, final int offset) {
		final double ret = Math.sqrt(varBase(data, offset));
		logger.info("stddev = {}", ret);
		return ret;
	}
	public static double stddevSample(final double[] data) {
		return stddevBase(data, 1);
	}
	public static double stddevPopulation(final double[] data) {
		return stddevBase(data, 0);
	}
	public static double[] rateOfReturn(final double[] data) {
		final int N = data.length;
		double[] ret = new double[N - 1];
		
		for(int i = 1; i < N; i++) {
			ret[i - 1] = (data[i] / data[i - 1]) - 1.0;
			//logger.info("ret[{}] = {}", i - 1, ret[i - 1]);
		}
		return ret;
	}
	public static double[] rateOfReturnLog(final double[] data) {
		final int N = data.length;
		double[] ret = new double[N - 1];
		
		for(int i = 1; i < N; i++) {
			ret[i - 1] = Math.log(data[i] / data[i - 1]);
			//logger.info("ret[{}] = {}", i - 1, ret[i - 1]);
		}
		return ret;
	}
	public static double hv(final double[] S) {
		final double[] R = rateOfReturn(S);
		final double ret = stddevSample(R);
		logger.info("hv = {}", ret);
		return ret;
	}
	public static double hvLog(final double[] S) {
		final double[] R = rateOfReturnLog(S);
		final double ret = stddevSample(R);
		logger.info("hv = {}", ret);
		return ret;
	}
